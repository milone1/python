#lista de  dias de la semana.
dias = ["lunes", "martes", "miercoles", "jueves", "viernes"]

#Imprimir listas.
print(dias)

#Acceder al primer valormediante su indice.
print(dias[1])

#Acceder a la lista numero negativo.
print(dias[-2])

#Acceder a la lista desde el indice 0 hasta el 2.
print(dias[0: 2])

#Acceder desde el inicio hasta la 
print(dias[:3])

#Acceder desde una posicion hacia el final.
print(dias[3:])
